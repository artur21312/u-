'use strict';
function prof() {
    alert("Ласкаво просимо до програми вибору професії.");

    let технічніНавички = prompt("Чи любите ви працювати з технікою або програмуванням? (так/ні)").toLowerCase();

    if (технічніНавички === "так") {
        let математика = prompt("Чи подобається вам математика? (так/ні)").toLowerCase();

        if (математика === "так") {
            let інженерія = prompt("Чи цікавить вас інженерія? (так/ні)").toLowerCase();
            if (інженерія === "так") {
                alert("Ви можете стати інженером або програмістом.");
            } else {
                alert("Вам може підійти професія в області аналітики даних або математики.");
            }
        } else {
            let дизайн = prompt("Чи подобається вам дизайн або креативна робота? (так/ні)").toLowerCase();
            if (дизайн === "так") {
                alert("Вам може підійти професія веб-дизайнера або графічного дизайнера.");
            } else {
                alert("Вам може підійти професія технічного спеціаліста або системного адміністратора.");
            }
        }
    } else if (технічніНавички === "ні") {
        let соціальніНавички = prompt("Чи любите ви спілкуватися з людьми? (так/ні)").toLowerCase();

        if (соціальніНавички === "так") {
            let педагогіка = prompt("Чи цікавить вас навчання інших? (так/ні)").toLowerCase();
            if (педагогіка === "так") {
                alert("Вам може підійти професія вчителя або викладача.");
            } else {
                let медицина = prompt("Чи цікавить вас допомога іншим людям? (так/ні)").toLowerCase();
                if (медицина === "так") {
                    alert("Ви можете стати лікарем або медсестрою.");
                } else {
                    alert("Вам може підійти професія менеджера або консультанта.");
                }
            }
        } else {
            let творчість = prompt("Чи подобається вам творча робота? (так/ні)").toLowerCase();

            if (творчість === "так") {
                alert("Вам може підійти професія письменника, художника або музиканта.");
            } else {
                alert("Вам може підійти професія в області ручної праці або сільського господарства.");
            }
        }
    } else {
        alert("Невірна відповідь, спробуйте знову.");
    }
}

prof();
